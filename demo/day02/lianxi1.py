#成绩0-59不及格，60-70及格 70-80 良好 80以上是优秀    [90,100,81,84]判断下改组成绩是否全部都是优秀
n =0#0表示成绩都在80分以上,1表示不全在80以上
for a in [90,100,81,84]:
    if(a)<80:
        n=1
if(n==0) :
    print("全是优秀")
else:
    print("不全是优秀")

